    private void bt_borrarMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_bt_borrarMousePressed
        if(mascotas.size()>0){
            int showConfirmDialog = JOptionPane.showConfirmDialog(null, "Seguro deseas liberar TODAS las mascotas", "seguro?", 0, 1);
                try {
                    if(showConfirmDialog!=1)
                        borrarTodo();
                    
                }catch(Exception e){

                }
        }else{
            JOptionPane.showMessageDialog(null, "No hay mascotas que liberar", "no hay nada", 1);
        }
    }//GEN-LAST:event_bt_borrarMousePressed

