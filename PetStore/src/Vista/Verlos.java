    private void bt_borrarMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_bt_borrarMousePressed
        if(mascotas.size()>0){
            int showConfirmDialog = JOptionPane.showConfirmDialog(null, "Seguro deseas retirar tu mascota?", "seguro?", 0, 1);
            try {
                if(showConfirmDialog!=1)
                    borrar(pokemon.getNombreMascota());
                
                if(mascotas.size()>0){
                    n=0;
                    ver();
                }else if(mascotas.size()==0){
                    nombre.setText("(No hay mascotas todavia)");
                    edad.setText("N/A");
                    raza.setText("N/A");
                    dueno.setText("N/A");
                    contacto.setText("N/A");
                    posicion.setText("");
                    pokemo.setIcon(no_disponible.getIcon());
                }
                    
            } catch (IOException ex) {
                Logger.getLogger(Verlos.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }
    }//GEN-LAST:event_bt_borrarMousePressed
