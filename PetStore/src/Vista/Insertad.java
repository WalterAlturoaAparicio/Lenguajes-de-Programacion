    private void bt_registrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_registrarActionPerformed
        name = nombre.getText();
        race = raza.getText();
        age = edad.getText();
        owner = dueno.getText();
        contact = contacto.getText();
        icon = k.get(x);
        validar();
        LogicaMascotas.crear(pet, name, race, age, owner, contact, icon);
        this.nombre.setText("");
        this.raza.setText("");
        this.edad.setText("");
        this.dueno.setText("");
        this.contacto.setText("");
        x =(int) (Math.random()*10);
        JOptionPane.showMessageDialog(null, "Mascota Ingresada", "mascota ingresada exitasamente", 1);
    }//GEN-LAST:event_bt_registrarActionPerformed

