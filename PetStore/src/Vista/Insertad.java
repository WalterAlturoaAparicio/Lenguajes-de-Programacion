    public void validar(){
        if(nombre.getText().equals("") && raza.getText().equals("") && edad.getText().equals("") && dueno.getText().equals("") && contacto.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Debe llenar todos los campos", "Aviso!", 1);
        }else if(nombre.getText().equals("") || raza.getText().equals("") || edad.getText().equals("") || dueno.getText().equals("") || contacto.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Debe llenar todos los campos", "Aviso!", 1);
        }
    }
